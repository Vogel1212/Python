# Docs Python
# Usando Python como calculadora

print("Welcome to Calculator")

number_1 = int(input('Enter your first number: '))
number_2 = int(input('Enter your second number: '))

# Addition
print('{} + {} = '.format(number_1, number_2))
print(number_1 + number_2)

# Subtraction
print('{} - {} = '.format(number_1, number_2))
print(number_1 - number_2)

# Division
print('{} / {} = '.format(number_1, number_2))
print(number_1 / number_2)

# Multiplication
print('{} * {} = '.format(number_1, number_2))
print(number_1 * number_2)

# Exponenciaçao
print('{} ** {} = '.format(number_1, number_2))
print(number_1 ** number_2)

# Módulo
print('{} % {} = '.format(number_1, number_2))
print(number_1 % number_2)

# Divisão Inteira
print('{} // {} = '.format(number_1, number_2))
print(number_1 // number_2)


